// ─── applyBracketRefresh ─────────────────────────────────────────────────────

describe("applyBracketRefresh()", () => {
	it("merges updated bracket data into the matching tournament", () => {
		const now = new Date("2026-02-25T12:00:00Z");
		const config = {
			name: "CS2 Major 2026",
			sport: "esports",
			autoGenerated: true,
			tournaments: [
				{
					id: "cs2-major-2026",
					name: "CS2 Major 2026",
					startDate: "2026-02-24",
					endDate: "2026-02-28",
					bracket: {
						playoffs: {
							upperBracket: [
								{
									round: "Round 1",
									matches: [{ team1: "NaVi", team2: "FaZe", status: "pending" }],
								},
							],
						},
					},
				},
			],
		};

		const bracketResult = {
			bracket: {
				playoffs: {
					upperBracket: [
						{
							round: "Round 1",
							matches: [
								{ team1: "NaVi", team2: "FaZe", score: "2-0", winner: "NaVi", status: "completed" },
							],
						},
					],
				},
				// focusTeamPath lives at the bracket root, not nested under playoffs
				focusTeamPath: {
					team: "NaVi",
					status: "Upper Bracket Round 2",
					completed: [{ stage: "UB R1", opponent: "FaZe", result: "W 2-0" }],
				},
			},
		};

		const updated = applyBracketRefresh(config, "cs2-major-2026", bracketResult, now);

		const t = updated.tournaments[0];
		expect(t.bracket.playoffs.upperBracket[0].matches[0].winner).toBe("NaVi");
		expect(t.bracket.playoffs.upperBracket[0].matches[0].status).toBe("completed");
		expect(t.bracket.playoffs.upperBracket[0].matches[0].score).toBe("2-0");
		expect(t.bracket.focusTeamPath.team).toBe("NaVi");
	});

	it("updates lastResearched timestamp", () => {
		const now = new Date("2026-02-25T14:30:00Z");
		const config = {
			name: "Test",
			tournaments: [
				{
					id: "test-tourney",
					name: "Test Tourney",
					bracket: { playoffs: {} },
				},
			],
		};

		const updated = applyBracketRefresh(config, "test-tourney", { bracket: { updated: true } }, now);
		expect(updated.lastResearched).toMatch(/2026-02-25/);
	});

	it("returns config unchanged when tournament id is not found", () => {
		const config = {
			name: "Test",
			tournaments: [
				{
					id: "real-tourney",
					name: "Real Tourney",
					bracket: { playoffs: {} },
				},
			],
		};

		const updated = applyBracketRefresh(config, "nonexistent-id", { bracket: { foo: "bar" } });
		expect(updated.tournaments[0].bracket).not.toHaveProperty("foo");
	});

	it("returns config unchanged when tournaments array is missing", () => {
		const config = {
			name: "No Tournaments Config",
			events: [],
		};

		const updated = applyBracketRefresh(config, "some-id", { bracket: { playoffs: {} } });
		expect(updated.tournaments).toBeUndefined();
	});

	it("does not mutate the original config's tournaments array reference", () => {
		const originalBracket = { playoffs: { upperBracket: [] } };
		const config = {
			name: "Test",
			tournaments: [
				{
					id: "t1",
					name: "T1",
					bracket: originalBracket,
				},
			],
		};

		const newBracket = { playoffs: { upperBracket: [{ round: "Final", matches: [] }] } };
		applyBracketRefresh(config, "t1", { bracket: newBracket });

		// The returned updated config should have new bracket
		// but we're testing the function doesn't throw or corrupt
		expect(originalBracket.playoffs.upperBracket).toHaveLength(0);
	});

	it("replaces the entire bracket object with the result bracket", () => {
		const config = {
			name: "Test",
			tournaments: [
				{
					id: "t1",
					name: "T1",
					bracket: {
						groups: { A: { name: "Group A" } },
						playoffs: { format: "Old format" },
					},
				},
			],
		};

		const newBracket = {
			groups: { A: { name: "Group A", result: { "1st": "TeamX" } } },
			playoffs: { format: "New format", upperBracket: [] },
		};

		const updated = applyBracketRefresh(config, "t1", { bracket: newBracket });
		const t = updated.tournaments[0];
		expect(t.bracket.playoffs.format).toBe("New format");
		expect(t.bracket.groups.A.result["1st"]).toBe("TeamX");
	});
});
