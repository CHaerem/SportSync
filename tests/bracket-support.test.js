import { describe, it, expect, beforeEach, afterEach } from "vitest";
import fs from "fs";
import path from "path";
import os from "os";
import { execSync } from "child_process";
import {
	findResearchTasks,
	buildResearchPrompt,
	applyBracketRefresh,
} from "../scripts/discover-events.js";

let tmpDataDir;
let tmpConfigDir;

function writeJson(filePath, data) {
	fs.mkdirSync(path.dirname(filePath), { recursive: true });
	fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
}

function runBuildEvents() {
	execSync("node scripts/build-events.js", {
		cwd: process.cwd(),
		env: {
			...process.env,
			SPORTSYNC_DATA_DIR: tmpDataDir,
			SPORTSYNC_CONFIG_DIR: tmpConfigDir,
		},
	});
}

function readJson(filePath) {
	return JSON.parse(fs.readFileSync(filePath, "utf-8"));
}

beforeEach(() => {
	tmpDataDir = fs.mkdtempSync(path.join(os.tmpdir(), "bracket-data-"));
	tmpConfigDir = fs.mkdtempSync(path.join(os.tmpdir(), "bracket-config-"));
});

afterEach(() => {
	fs.rmSync(tmpDataDir, { recursive: true, force: true });
	fs.rmSync(tmpConfigDir, { recursive: true, force: true });
});

